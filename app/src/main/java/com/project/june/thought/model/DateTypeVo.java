package com.project.june.thought.model;

/**
 * 存储多种时间类型的类
 * Created by June on 2017/3/25.
 */
public class DateTypeVo {

    private String MonthDateType;
    private String yearMonthType;

    public String getMonthDateType() {
        return MonthDateType;
    }

    public void setMonthDateType(String monthDateType) {
        MonthDateType = monthDateType;
    }

    public String getYearMonthType() {
        return yearMonthType;
    }

    public void setYearMonthType(String yearMonthType) {
        this.yearMonthType = yearMonthType;
    }
}
